"""
TriNetX PancPump Study - Script 08: Index Admission Length of Stay

Iteration-friendly LOS analysis. Loads matched pair lists generated by
Script 07 (psm_pairs_*.csv) and skips PSM entirely. Only requires:
  - master_patient_table.csv (for index dates)
  - encounter.csv (for inpatient admissions)

If pair caches aren't found, the script falls back to running PSM,
loading lab_clean and diagnosis as needed.

LOS = end_date - start_date in days for the inpatient encounter
(type IMP) that brackets the index date. Coalesces raw dates with
TriNetX-derived dates when raw dates are missing.

Compared via Wilcoxon rank-sum and t-test on log-LOS.

PI: Paul Kuo
"""

