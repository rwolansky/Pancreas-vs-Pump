def era_slug(era_label):
    return era_label.lower().replace(' ', '_')

def parse_dates(s):
    """Parse YYYYMMDD dates. Handles float coercion that happens when pandas
    reads an integer column containing NaN values (so 20210107 becomes
    20210107.0). Strips trailing '.0' before YYYYMMDD parsing."""
    s_str = s.astype(str).str.replace(r'\.0$', '', regex=True)
    d = pd.to_datetime(s_str, format='%Y%m%d', errors='coerce')
    if d.notna().sum() > len(d) * 0.3:
        return d
    return pd.to_datetime(s, errors='coerce')

