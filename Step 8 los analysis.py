# LOS HELPER
# =============================================================================
def compute_los(pid, index_date):
    pid_encs = inpatient[inpatient['patient_id'] == pid]
    if len(pid_encs) == 0:
        return np.nan
    starts = pid_encs['start_dt']
    ends   = pid_encs['end_dt']
    mask = (starts <= index_date) & (ends >= index_date)
    if mask.sum() == 0:
        # Fallback: nearest start within 7 days before index
        before = pid_encs[(starts <= index_date) &
                           ((index_date - starts).dt.days <= 7)]
        if len(before) == 0: return np.nan
        row = before.loc[before['start_dt'].idxmax()]
    else:
        row = pid_encs[mask].iloc[0]
    if pd.isna(row['start_dt']) or pd.isna(row['end_dt']):
        return np.nan
    return max((row['end_dt'] - row['start_dt']).days, 1)

