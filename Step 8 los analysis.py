    PSM_DX_FLAGS = {
        'dm_type1':'E10', 'dm_type2':'E11', 'hypertension':'I10', 'cad':'I25',
        'heart_failure':'I50', 'pvd':'I73', 'dyslipidemia':'E78',
        'obesity':'E66', 'ckd':'N18', 'anemia':'D64',
    }
    HBAC1C_LOINCS = ['4548-4', '17856-6', '59261-8']
    CREAT_LOINCS  = ['2160-0', '38483-4']
